package menti2;

public class Day20 {

    public static void main(String[] args) {

        Byte angka = 20;// Mendeklarasikan dan menginisialisasi objek Byte

        System.out.println("nilai Byte : " + angka);// Mencetak nilai objek Byte

        int tukar = angka.intValue();// Menggunakan metode untuk mengubah nilai objek Byte ke tipe int
        System.out.println("Di ubah menjadi tipe data int : " + tukar);
    }
}
