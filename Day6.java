package menti;

public class Day6 {

    public static void main(String[] args) {

        System.out.println("Ketinggian tempat : ");

        // Untuk Deklarasi variabel dengan tipe data short
        short tinggi;

        // Untuk Inisialisasi variabel atau memberikan nilai awal
        tinggi = 30;
        System.out.print("Ketinggian awal : ");
        System.out.println(tinggi + " meter");

        // Untuk Update nilai variabel
        tinggi = 45;
        System.out.print("Ketinggian setelah pendakian : ");
        System.out.println(tinggi + " meter");
    }
}
